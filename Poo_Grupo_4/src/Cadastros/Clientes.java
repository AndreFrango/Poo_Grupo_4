package Cadastros;
public class Clientes {
    String nome;
    String email;
    String endereco;
    String cpf;
    String rg;
    String telefone;

public void setClientes(String nome, String email, String endereco, String cpf, String rg, String telefone){
this.setNome(nome);
this.setEmail(email);
this.setEndereco(endereco);
this.setCpf(cpf);
this.setRg(rg);
this.setTelefone(telefone);
}

}
